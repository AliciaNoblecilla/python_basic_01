""" operadores aritmeticos en python"""

"""operador modulo: % """
""" arroja el residuo de una division"""
modulo_1 = 39 % 15
modulo_2 = 45 % 2
print("El resultado de mi modulo_1 es: {} y de mi modulo_2 es: {}".format(modulo_1,modulo_2))


""" OPERADOR POTENCIA 
 primera forma: ** """

potencia_1 = 10 ** 3
print(" El resultado de mi potencia es: {}".format(potencia_1))

""" segunda forma: pow 
siempre nos pedira dos valores primero es la base y la segunda es el exponente """

potencia_2 = pow(20, 4)
potencia_3 = pow(9, 2)

print(" el resultado de mis dos potencias es de {} y {}".format(potencia_2,potencia_3))

